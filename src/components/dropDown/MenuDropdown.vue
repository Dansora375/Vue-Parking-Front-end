  name: 'MenuDropDown',
  // mixins: [clickaway],
  provide () {
    return {
      estadoMenu: this.estadoMenu
    }
  },

  data () {
    return {
      estadoMenu: {
        activo: false
      }
    }
  },
  methods: {
    toggle () {
      // console.log(this.estadoMenu);
      this.estadoMenu.activo = !this.estadoMenu.activo
    },
    away () {
      this.estadoMenu.activo = false
    }
  }
